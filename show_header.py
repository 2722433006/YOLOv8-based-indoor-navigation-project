# 这个脚本唯一的目的就是打印出YOLOv8评估结果的标题行

# 我们使用 f-string 和对齐来让格式更美观
# {:<15} 的意思是：左对齐，并且占据15个字符的宽度
# {:>10} 的意思是：右对齐，并且占据10个字符的宽度

header = (
    f"{'Class':<20}"  # 类别名称，左对齐，占20位
    f"{'Images':>10}" # 图片数，右对齐，占10位
    f"{'Instances':>12}" # 实例数，右对齐，占12位
    f"{'P':>10}"      # 精确率
    f"{'R':>10}"      # 召回率
    f"{'mAP@.50':>12}"  # mAP50
    f"{'mAP@.50-.95':>15}" # mAP50-95
)

print(header)
print("-" * 100) # 打印一条分隔线，让它看起来更像一个表格
